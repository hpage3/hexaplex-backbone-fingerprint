"""Audit reproducible pipeline coverage for manuscript-facing reports.

The audit scans report-like Markdown documents and classifies whether each is
covered by the current publication pipeline, reusable as an existing output, or
exploratory/needs review. It is deliberately conservative: unknown reports are
not promoted to manuscript support without an explicit mapping.
"""

from __future__ import annotations

import argparse
from dataclasses import dataclass
from pathlib import Path

import pandas as pd


MANUSCRIPT_ROOT = Path("outputs/manuscript")
OUT_CSV = MANUSCRIPT_ROOT / "metrics" / "manuscript_pipeline_coverage.csv"
OUT_REPORT = MANUSCRIPT_ROOT / "reports" / "manuscript_pipeline_coverage_report.md"


@dataclass(frozen=True)
class KnownReport:
    """Known manuscript-supporting report mapping."""

    report_name: str
    manuscript_items: str
    generating_script: str
    included_in_publication_pipeline: bool
    expected_source_path: str
    notes: str


